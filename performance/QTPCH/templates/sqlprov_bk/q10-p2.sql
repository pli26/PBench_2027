SELECT "orderby"."tuid" AS "tuid", 
       "subquery5"."c_custkey" AS "c_custkey", 
       "subquery5"."c_name" AS "c_name", 
       "subquery5"."revenue" AS "revenue", 
       "subquery5"."c_acctbal" AS "c_acctbal", 
       "subquery5"."n_name" AS "n_name", 
       "subquery5"."c_address" AS "c_address", 
       "subquery5"."c_phone" AS "c_phone", 
       "subquery5"."c_comment" AS "c_comment"
FROM (SELECT "group"."tuid" AS "tuid", 
             concat_agg("subquery4"."c_custkey") AS "c_custkey", 
             concat_agg("subquery4"."c_name") AS "c_name", 
             concat_agg(("subquery4"."l_extendedprice") ||
                        ((ARRAY[] :: int4[]) ||
                         ("subquery4"."l_discount"))) AS "revenue", 
             concat_agg("subquery4"."c_acctbal") AS "c_acctbal", 
             concat_agg("subquery4"."n_name") AS "n_name", 
             concat_agg("subquery4"."c_address") AS "c_address", 
             concat_agg("subquery4"."c_phone") AS "c_phone", 
             concat_agg("subquery4"."c_comment") AS "c_comment"
      FROM (SELECT "join"."tuid" AS "tuid", 
                   "RTE0"."c_custkey" AS "c_custkey", 
                   "RTE0"."c_name" AS "c_name", 
                   "RTE2"."l_extendedprice" AS "l_extendedprice", 
                   "RTE2"."l_discount" AS "l_discount", 
                   "RTE0"."c_acctbal" AS "c_acctbal", 
                   "RTE3"."n_name" AS "n_name", 
                   "RTE0"."c_address" AS "c_address", 
                   "RTE0"."c_phone" AS "c_phone", 
                   "RTE0"."c_comment" AS "c_comment"
            FROM customer_2 AS "RTE0"("tuid", 
                                      "c_custkey", 
                                      "c_name", 
                                      "c_address", 
                                      "c_nationkey", 
                                      "c_phone", 
                                      "c_acctbal", 
                                      "c_mktsegment", 
                                      "c_comment"), 
                 orders_2 AS "RTE1"("tuid", 
                                    "o_orderkey", 
                                    "o_custkey", 
                                    "o_orderstatus", 
                                    "o_totalprice", 
                                    "o_orderdate", 
                                    "o_orderpriority", 
                                    "o_clerk", 
                                    "o_shippriority", 
                                    "o_comment"), 
                 lineitem_2 AS "RTE2"("tuid", 
                                      "l_orderkey", 
                                      "l_partkey", 
                                      "l_suppkey", 
                                      "l_linenumber", 
                                      "l_quantity", 
                                      "l_extendedprice", 
                                      "l_discount", 
                                      "l_tax", 
                                      "l_returnflag", 
                                      "l_linestatus", 
                                      "l_shipdate", 
                                      "l_commitdate", 
                                      "l_receiptdate", 
                                      "l_shipinstruct", 
                                      "l_shipmode", 
                                      "l_comment"), 
                 nation_2 AS "RTE3"("tuid", 
                                    "n_nationkey", 
                                    "n_name", 
                                    "n_regionkey", 
                                    "n_comment"), 
                 LATERAL readjoin(1, 
                                  "RTE0"."tuid", 
                                  "RTE1"."tuid", 
                                  "RTE2"."tuid", 
                                  "RTE3"."tuid") AS "join"("tuid")) AS "subquery4"("tuid", 
                                                                                   "c_custkey", 
                                                                                   "c_name", 
                                                                                   "l_extendedprice", 
                                                                                   "l_discount", 
                                                                                   "c_acctbal", 
                                                                                   "n_name", 
                                                                                   "c_address", 
                                                                                   "c_phone", 
                                                                                   "c_comment"), 
           LATERAL readaggregation(2, 
                                   "subquery4"."tuid") AS "group"("tuid")
      GROUP BY ("group"."tuid")) AS "subquery5"("tuid", 
                                                "c_custkey", 
                                                "c_name", 
                                                "revenue", 
                                                "c_acctbal", 
                                                "n_name", 
                                                "c_address", 
                                                "c_phone", 
                                                "c_comment"), 
     LATERAL readorderby(4, 
                         "subquery5"."tuid") AS "orderby"("tuid");