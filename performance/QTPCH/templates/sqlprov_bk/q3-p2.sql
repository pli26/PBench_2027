SELECT "orderby"."tuid" AS "tuid", 
       "subquery4"."l_orderkey" AS "l_orderkey", 
       "subquery4"."revenue" AS "revenue", 
       "subquery4"."o_orderdate" AS "o_orderdate", 
       "subquery4"."o_shippriority" AS "o_shippriority"
FROM (SELECT "group"."tuid" AS "tuid", 
             concat_agg("subquery3"."l_orderkey") AS "l_orderkey", 
             concat_agg(("subquery3"."l_extendedprice") ||
                        ((ARRAY[] :: int4[]) ||
                         ("subquery3"."l_discount"))) AS "revenue", 
             concat_agg("subquery3"."o_orderdate") AS "o_orderdate", 
             concat_agg("subquery3"."o_shippriority") AS "o_shippriority"
      FROM (SELECT "join"."tuid" AS "tuid", 
                   "RTE2"."l_orderkey" AS "l_orderkey", 
                   "RTE2"."l_extendedprice" AS "l_extendedprice", 
                   "RTE2"."l_discount" AS "l_discount", 
                   "RTE1"."o_orderdate" AS "o_orderdate", 
                   "RTE1"."o_shippriority" AS "o_shippriority"
            FROM customer_2 AS "RTE0"("tuid", 
                                      "c_custkey", 
                                      "c_name", 
                                      "c_address", 
                                      "c_nationkey", 
                                      "c_phone", 
                                      "c_acctbal", 
                                      "c_mktsegment", 
                                      "c_comment"), 
                 orders_2 AS "RTE1"("tuid", 
                                    "o_orderkey", 
                                    "o_custkey", 
                                    "o_orderstatus", 
                                    "o_totalprice", 
                                    "o_orderdate", 
                                    "o_orderpriority", 
                                    "o_clerk", 
                                    "o_shippriority", 
                                    "o_comment"), 
                 lineitem_2 AS "RTE2"("tuid", 
                                      "l_orderkey", 
                                      "l_partkey", 
                                      "l_suppkey", 
                                      "l_linenumber", 
                                      "l_quantity", 
                                      "l_extendedprice", 
                                      "l_discount", 
                                      "l_tax", 
                                      "l_returnflag", 
                                      "l_linestatus", 
                                      "l_shipdate", 
                                      "l_commitdate", 
                                      "l_receiptdate", 
                                      "l_shipinstruct", 
                                      "l_shipmode", 
                                      "l_comment"), 
                 LATERAL readjoin(1, 
                                  "RTE0"."tuid", 
                                  "RTE1"."tuid", 
                                  "RTE2"."tuid") AS "join"("tuid")) AS "subquery3"("tuid", 
                                                                                   "l_orderkey", 
                                                                                   "l_extendedprice", 
                                                                                   "l_discount", 
                                                                                   "o_orderdate", 
                                                                                   "o_shippriority"), 
           LATERAL readaggregation(2, 
                                   "subquery3"."tuid") AS "group"("tuid")
      GROUP BY ("group"."tuid")) AS "subquery4"("tuid", 
                                                "l_orderkey", 
                                                "revenue", 
                                                "o_orderdate", 
                                                "o_shippriority"), 
     LATERAL readorderby(4, 
                         "subquery4"."tuid") AS "orderby"("tuid");